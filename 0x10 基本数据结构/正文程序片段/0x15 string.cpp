// KMP：字符串总长 n - next[n] 就是最小循环节（可能不完美覆盖）的长度
// 如果整个字符串可以被其最小循环节长度 s 完美覆盖（复制多次），则该串所有循环节长度都是 s 的整数倍
// next[i] 表示 “A 中以 i 结尾的非前缀子串” 与 “A 的前缀” 能够匹配的最大长度
// 假设 next[1 ~ i - 1] 已求出，不断求 next[i]；j 表示最长匹配长度，初始化为 0
next[1] = 0;
for (int i = 2, j = 0; i <= n; i ++ ) { // 这里next是在建立A自身子串的对应关系，所以初始化i=2
  // 不断尝试扩展匹配长度 j，如果扩展失败，令 j = next[j]，直至 j为 0（从头匹配）
  while (j > 0 && a[i] != a[j + 1]) j = next[j];
  // 如果扩展成功，匹配长度 j + 1；next[i]的值就是j
  if (a[i] == a[j + 1]) j ++ ;
  next[i] = j;
}

