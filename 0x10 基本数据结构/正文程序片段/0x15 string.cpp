// 最小表示法：找循环同构的 n 个字符串中字典序最小的一个
// B[i] 表示从 i 开始的循环同构字符串，即：S[i ~ n] + S[1 ~ i - 1]
// 可通过拼接 SS，则 B[i] = SS[i ~ i + n - 1]
int n = strlen(s + 1);
for (int i = 1; i <= n; i ++ ) s[n + i] = s[i];
int i = 1, j = 2, k; // 初始化
while (i <= n && j <= n) {
  // 1. 如果扫描了 n 个字符后 B[i] 与 B[j] 仍然相等，说明 S 有更小的循环元
  for (k = 0; k < n && s[i + k] == s[j + k]; k ++ );
  if (k == n) break; // s 形如 "aaa" 时，它的循环元已扫描完成
  // 2. 如果在 i + k 与 j + k 处发现不相等
  if (s[i + k] > s[j + k]) {
    i = i + k + 1;
    if (i == j) i ++ ;
  } else {
    j = j + k + 1;
    if (i == j) j ++ ;
  }
}
ans = min(i, j); // B[ans]是最小表示
